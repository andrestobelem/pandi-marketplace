const GLYPH_WIDTH = 5;
const GLYPH_HEIGHT = 7;

/** One character as 5 columns (left-to-right), each a 7-element bottom-to-top
 * boolean array (index 0 = device row 1, index 6 = device row 7). */
export function glyphColumns(char: string): boolean[][] {
  const rows = GLYPH_ROWS[char.toUpperCase()];
  if (!rows) {
    throw new Error(`Unsupported character ${JSON.stringify(char)}. Available: ${Object.keys(GLYPH_ROWS).join(", ")}`);
  }
  return Array.from({ length: GLYPH_WIDTH }, (_, col) =>
    Array.from({ length: GLYPH_HEIGHT }, (_, deviceRow) => rows[GLYPH_HEIGHT - 1 - deviceRow]![col] === "#"),
  );
}

const SPACER_COLUMN: boolean[] = Array(GLYPH_HEIGHT).fill(false);

/** Every character's glyphColumns concatenated left-to-right, with one blank
 * spacer column between characters (none before the first or after the last). */
export function textColumns(text: string): boolean[][] {
  return Array.from(text)
    .map((char) => glyphColumns(char))
    .flatMap((columns, i) => (i === 0 ? columns : [SPACER_COLUMN, ...columns]));
}
